                string.Empty,
                ProcedureState: new TransactionProcedureState(
                    targetSchema,
                    procedureName.NormalizeName(),
                    previousDefinition)));
        }

        Db.RemoveProcedure(procedureName, targetSchema);
        SqlQueryParser.ClearAstCache();
    }

